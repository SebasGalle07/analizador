async function runDashboard(event) {
  if (event) event.preventDefault();
  setStatus("Ejecutando metricas y generando graficas...");
  const warn = (label) => (err) => console.warn(`${label}:`, err.message);
  try {
    if (PAGE === "similarity") {
      await refreshSimilarity().catch(warn("similitud"));
    } else if (PAGE === "patterns") {
      await Promise.allSettled([
        refreshPatterns().catch(warn("patrones")),
        refreshRisk().catch(warn("riesgo")),
      ]);
    } else if (PAGE === "visualization") {
      await Promise.allSettled([
        refreshCandlestick().catch(warn("velas")),
        refreshCorrelation().catch(warn("correlacion")),
      ]);
    } else if (PAGE === "etl") {
      await loadOverview();
    } else {
      await Promise.allSettled([
        refreshSimilarity().catch(warn("similitud")),
        refreshCandlestick().catch(warn("velas")),
        refreshRisk().catch(warn("riesgo")),
        refreshPatterns().catch(warn("patrones")),
        refreshCorrelation().catch(warn("correlacion")),
      ]);
    }
    setStatus("Analisis actualizado.");
  } catch (err) {
    setStatus(err.message, "error");
  }
}

async function rebuildDataset() {
  setBusy(true);
  startStatusSequence([
    "ETL | reconstruyendo dataset",
    "ETL | descargando activos y validando respuesta",
    "ETL | limpiando registros e inconsistencias",
    "ETL | unificando calendarios bursatiles",
    "ETL | guardando CSV maestro y reporte",
  ]);
  try {
    setEtlLoading(true, "Reconstruyendo ETL", "Descargando y unificando datos...");
    const data = await fetchJson("/dataset/build", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ years: 5, nombre_archivo: "data/processed/dataset_maestro.csv" }),
    });
    await loadOverview();
    await runDashboard();
    stopStatusSequence();
    setStage("ETL completado", `${data.rows} filas generadas`, "info");
  } finally {
    stopStatusSequence();
    setEtlLoading(false);
    setBusy(false);
  }
}

