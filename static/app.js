function setStatus(msg, type = "info") {
  const box = $("#status-box");
  if (!box) return;
  box.textContent = msg;
  box.dataset.type = type;
}

function setBusy(isBusy) {
  const controls = document.querySelectorAll("button, input, select");
  controls.forEach((el) => {
    if (el.id === "download-report") return;
    if (el.classList.contains("module-btn")) return;
    el.disabled = isBusy;
  });
}

function startStatusSequence(messages, stepMs = 900, type = "info") {
  stopStatusSequence();
  let index = 0;
  if (messages.length > 0) {
    setStatus(messages[0], type);
  }
  state.statusTimer = setInterval(() => {
    index = Math.min(index + 1, messages.length - 1);
    if (messages[index]) {
      setStatus(messages[index], type);
    }
  }, stepMs);
}

function stopStatusSequence() {
  if (state.statusTimer) {
    clearInterval(state.statusTimer);
    state.statusTimer = null;
  }
}

function setStage(label, detail, type = "info") {
  const message = detail ? `${label} | ${detail}` : label;
  setStatus(message, type);
}

function setEtlLoading(isLoading, title = "Sincronizando ETL", text = "Preparando datos...") {
  const shell = $("#etl-stats-section");
  const overlay = $("#etl-loading-overlay");
  const titleBox = $("#etl-loading-title");
  const textBox = $("#etl-loading-text");
  if (!shell || !overlay) return;
  shell.classList.toggle("is-loading", isLoading);
  overlay.style.display = isLoading ? "flex" : "none";
  if (isLoading) {
    shell.hidden = false;
  }
  if (titleBox) titleBox.textContent = title;
  if (textBox) textBox.textContent = text;
}

function formatNumber(value, digits = 2) {
  return new Intl.NumberFormat("es-CO", {
    maximumFractionDigits: digits,
    minimumFractionDigits: digits,
  }).format(Number(value));
}

function formatPct(value, digits = 2) {
  return `${formatNumber(Number(value) * 100, digits)}%`;
}

function cacheBust(url) {
  const sep = url.includes("?") ? "&" : "?";
  return `${url}${sep}t=${Date.now()}`;
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function fetchJson(url, options) {
  const response = await fetch(url, options);
  const data = await response.json();
  if (!response.ok) throw new Error(data.detail || "Solicitud fallida.");
  return data;
}

function fillSelect(selector, symbols, preferred) {
  const select = $(selector);
  if (!select) return;
  select.innerHTML = "";
  symbols.forEach((s) => {
    const opt = document.createElement("option");
    opt.value = s;
    opt.textContent = s;
    select.appendChild(opt);
  });
  if (preferred && symbols.includes(preferred)) select.value = preferred;
}

function activateModule(moduleName, { scroll = true } = {}) {
  state.activeModule = moduleName;

  document.querySelectorAll(".module-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.module === moduleName);
  });

  document.querySelectorAll(".module-panel").forEach((panel) => {
    panel.classList.toggle("module-panel-active", panel.dataset.module === moduleName);
  });

  if (scroll) {
    const target = document.querySelector(`.module-panel[data-module="${moduleName}"]`);
    if (target) {
      target.scrollIntoView({ behavior: "smooth", block: "start" });
    }
  }
}

